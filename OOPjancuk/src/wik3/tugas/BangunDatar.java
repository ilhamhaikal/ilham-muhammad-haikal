package wik3.tugas;

/**
 *
 * @author Asus
 */
public class BangunDatar {
    private int panjang;
    private int lebar;
    private int tinggi;
    private int alas;
    private int jarijari;

    public BangunDatar() {
    }

    public BangunDatar(int panjang, int lebar, int tinggi, int alas, int jarijari) {
        this.panjang = panjang;
        this.lebar = lebar;
        this.tinggi = tinggi;
        this.alas = alas;
        this.jarijari = jarijari;
    }

    public int getPanjang() {
        return panjang;
    }

    public void setPanjang(int panjang) {
        this.panjang = panjang;
    }

    public int getLebar() {
        return lebar;
    }

    public void setLebar(int lebar) {
        this.lebar = lebar;
    }

    public int getTinggi() {
        return tinggi;
    }

    public void setTinggi(int tinggi) {
        this.tinggi = tinggi;
    }

    public int getAlas() {
        return alas;
    }

    public void setAlas(int alas) {
        this.alas = alas;
    }

    public int getJarijari() {
        return jarijari;
    }

    public void setJarijari(int jarijari) {
        this.jarijari = jarijari;
    }
    
    public void luas() {
    }
    
    public void keliling() {
    }

}